    <h2>Intercultural Operation</h2>
    <div>
        <p>Weeding followed by top dressing and earthing up should be done at 30-35 days after
            sowing (DAS). In some cases, weeding may be required at 10-15 and 60-65 days
            after sowing (DAS).</p>
        <h3>Pest Management</h3>
        <h4>Major diseases and control measures:</h4>
        <h4>Fungal disease</h4>
        <p>A total of 57 diseases and disorders of potato have, so far, been recorded in
            Bangladesh. Among them, late blight (Phytophthora infestans (Mont de Bary), stem
            canker and Black scurf (Rhizoctonia solani) are the most important disease of potato
            in Bangladesh.</p>
        <h4>Late Blight </h4>
        <p>Late blight is the major limiting factor to increase potato production. In Bangladesh,
            the infection of late blight was first reported in 1922. It occurs every year under
            favourable condition (High humidity, low temperature and
            foggy weather) with varying intensities wherever potato is
            grown in the country. More than 25% yield is reduced due
            to late blight of potato every year. All the cultivated
            varieties in Bangladesh are more or less susceptible to late
            blight. Potato variety, Raja, Dheera and BARI TPS-1 and
            2 showed tolerant reaction to late blight disease. In
            general TPS progenies are more tolerant compared to
            tuber varieties. </p>
        <p>Late blight appears on leaves, petiole and stem as pale green and turns into brown to
            black water soaked spots under favorable condition.White cottony mycelial growth
            appears under the surface of infected leaf in the morning during high humidity and
            leaf wetness condition. Under cool, foggy and wet conditions, whole plant of the field
            rot and die rapidly within 4-5 days and tubers also rot and spread in storage. Under
            severe condition, emits foul smell from rotted leaf in the field. The sections from
            infected tuber appeared brown with necrotic tissues. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Use disease free certified seeds.</li>
            <li>Follow early planting and early harvesting (15 Novto 15 Feb).</li>
            <li>Weed management.</li>
            <li>Haulm pulling at 80 DAP.</li>
            <li>Under cool, foggy and wet conditions, spray should be continued with fungicides
                at 15 days interval. Any of the fungicides like Dithane M-45 or Indofil or Melody
                duo or Hamancozeb @ 0.2% at 15 days interval should be done.</li>
            <li>When symptoms appear, spraying with fungicides or mixture of fungicides at 7
                days interval </li>
            <li>Secure @ 1 g/ L water or</li>
            <li>Melody duo 2 g + Secure 1 g in 1 litre water or</li>
            <li>Acrobat MZ 2 g + Secure 1 g in 1 litre water or</li>
            <li>Acrobat MZ 2 g + Melody duo 2 g in 1 litre water or</li>
            <li>Melody duo 2 g + Dithane M-45 2 g in 1 litre water</li>
            <li>High ridge during earthing up.</li>
            <li>Restricted irrigation in the infected field.</li>
            <li>Do not cover the harvested tuber lots by infected potato debris.</li>
            <li>Sorting of infected tubers during storage.</li>
        </ul>
        <h3>Stem canker and Black Scurf</h3>
        <p>Causal fungus of this disease is<em> Rhizoctonia solaniand</em> now becoming a major disease
            of potato in Bangladesh.</p>
        <p>Aerial green tubers produce in the axes of petioles and branches.Sunken shallow
            brown cankers affect stolons and stem at or below the soil line. Ultimately, infected
            plant died and tuber size reduced.</p>
        <h3>Control measures:</h3>
        <ul>
            <li>Use of disease free seed.</li>
            <li>Destruction of crop debris.</li>
            <li>Crop rotation with cereals or leguminous crop.</li>
            <li>Seed treatment with Boric acid (3%) before storage.</li>
            <li>Rogue out of diseased plant from the field.</li>
            <li>Spraying with Bavistin (0.1%) at the base of the plant. </li>
            <li>Avoid excessive irrigation.</li>
        </ul>
        <h3>Bacterial disease management</h3>
        <p>Bacterial wilt (<em>Ralstonia solanacearum</em>), Common scab (<em>Streptomyces scabies</em>) and Soft
            rot (<em>Erwinia spp.</em>) are the most important bacterial diseases of potato in Bangladesh. </p>
        <h3>Bacterial Wilt</h3>
        <p>Initial symptoms of wilt may affect one side of a leaf first,
            or one branch and not another. Later symptoms are severe
            wilt and death. Vascular strands darken and in cross
            section a gray to brown slime exudes, except in mild cases
            which may be confirmed by the flow of milky white
            strands from a stem section immersed in clear water.</p>
        <h3>Control measures:</h3>
        <ul>
            <li>Rresistant source of bacterial wilt was not detected in Bangladesh.</li>
            <li>Use of certified seeds.</li>
            <li>Use of whole tuber.</li>
            <li>High ridge during earthing up.</li>
            <li>Crop rotation with non-solanaceous crop.</li>
            <li>Soil application with stable bleaching powder (SBP)@ 25-30 kg/ha. </li>
            <li>Intercropping with maize or wheat.</li>
            <li>Minimum irrigation should be maintained and irrigation should be stopped at the
                moment of disease expression.</li>
            <li>Diseased plant should be removed including surrounding soil and apply SBP at the
                rate of 20-25 kg/ha into the affected area.</li>
        </ul>
        <h3>Blackleg and Soft Rot</h3>
        <p>Black and slimy lesions most often progress
            up the stem from the soft rotted mother tuber.
            Young plants are commonly stunted and erect.
            Yellowing and upward rolling of leaflets may
            occur, finally wilting and death. Soft rot in
            field or storage often follows any type of tuber
            injury. Tissues become wet, cream to tan
            colored and soft, and are easily separated from
            healthy tissue. Emit foul smell.</p>

        <h3>Control measures:</h3>
        <ul>
            <li>Use diseased free seed.</li>
            <li>Prohibit excess irrigation in the field.</li>
            <li>Early cultivation should be done to avoid high temperature.</li>
            <li>Maintain proper grading before storage.</li>
            <li>Soil amendment with SBP @ 20-25 kg/ha.</li>
            <li>Tuber should be treated with 3.0% Boric acid solution before storing as seed tuber.</li>
            <li>Crop rotation with cereals or leguminous crop.</li>
            <li>Harvest in dry weather and proper care during harvest, transit & storage.</li>
            <li>Potato should be storedin dry and well ventilated room.</li>
            <li>Destruction of crop debris.</li>
        </ul>
        <h3>Common Scab</h3>
        <p>Scab appears on tubers as circulate to irregular lesions as
            corky-appearing, raised and pitted. They are variable in
            size and shape.Periderm turns brown and rough. Under
            severe condition, the periderm depressed and rotted.</p>
        <h3>Control measures:</h3>
        <ul>
            <li>Application of ammonium sulfate (equally in three
                installments) instead of urea as a nitrogen source
                proved most effective in reducing common scab disease of potato. </li>
            <li>It was also found that the incidence of common scab can be minimized effectively
                by maintaining proper soil moisture from 30 DAP to 60 DAP. </li>
            <li>Seed treatment with 3% boric acid before storage showed most effective in
                reducing scab disease of potato. </li>
            <li>Crop rotation with cereals or leguminous crop.</li>
            <li>Green manuring crop should be used.</li>
        </ul>
        <h3>Virus diseases</h3>
        <p>Viruses are the main problem in our country to reduce potato production. It decreased the
            production generation to generation of potato. There are several viruses remain in plant
            and tuber in latent condition. However, major viruses of potato are described below.</p>
        <h3>Potato Leaf Roll Virus Disease (PLRV)</h3>
        <p>Leaves become upward rolled which initiates from lower
            leaves. Rolled leaves are stiff and leathery and plant growth
            often stunted. PLRV is transmitted by aphid. Spots appear on
            leaves and defoliation occurred.</p>
        <h3>Potato Viruses Y (PVY)</h3>
        <p>Leaves became twisting and downward turning of leaflet
            margin. Plant growth severely stunted. Leaf and tuber size
            reduced markedly. Leaves look shiny.</p>
        <h3>Mosaics (Potato Viruses X, S and M) diseases</h3>
        <p>Mosaic symptoms may be caused in leaves of potatoes by
            several different viruses singly or in combination. Normally
            causes mosaic, crinkling, top necrosis, spotting or even leaf
            drop. Sometimes necrosis of petioles and leaf veins are
            occurred. Leaves are stiff and leathery and plant growth often
            stunted. Yield is reduced.</p>
        <h3>Control measures:</h3>
        <ul>
            <li>Use disease free certified seed.</li>
            <li>Follow early planting and early harvesting (15 Nov to 15 Feb).</li>
            <li>Isolation distance maintains.</li>
            <li>Weed management.</li>
            <li>Virus eradication through tissue culture.</li>
            <li>Indexing viruses through ELISA.</li>
            <li>Production of breeder seed under net-house.</li>
            <li>Haulm pulling at 80 DAP.</li>
            <li>Grading, sorting and storing in cold storage maintaining proper temp.</li>
            <li>Rouging all virus infected plants.</li>
            <li>Spraying systemic insecticides as Admire (0.05%) to control vector.</li>
        </ul>
        <h3>Major insects and control measures:</h3>
        <p>Potato is an important crop world wide. The yield of potato is greatly reduced due to
            attack of several insect pest and diseases, including several viruses which contribute
            to the degeneration of seed stocks. For better tuber crop production, good quality of
            seed materials needs to be supplied to the farmers. So, emphasis should be given to
            produce disease and insect free seed. Major insect pests of potato are Cutworm,
            Potato tuber moth, Aphid, Whitefly and Leafhopper.</p>
        <h3>Potato cutworm (Agrotis ipsilon) </h3>
        <p>Cutworm is a polyphagous and the most devastating soil pest.
            Yield is reduced up to 35- 40%by cutworm.Young caterpillars
            feed on leaves and later on stems. Mature caterpillars cause
            the most damage. They are capable of eating or destroying
            the entire plant. Some times they cut-off young seedlings at
            ground level during night. Caterpillars feed on tubers and
            roots, boring a wide shallow hole. </p>
        <h3>Control measures:</h3>
        <ul>
            <li>Hand picking of caterpillars at night by torch or very early morning.</li>
            <li>Flooding of the field for a few days before sowing can help to control cutworm
                caterpillars in the soil. </li>
            <li>Potato crops grown for seed should be monitored closely and good sanitation
                should be maintained.</li>
            <li>Use of poison bait by controlling cutworm.</li>
            <li>Use of insecticides Chlorpyrifos (Chlorpyrifos 48EC/Pyrifos 20EC @ 5ml/1L water
                at 15 days interval)/Carbofuran (Dursban 20EC and Furadan 5 G @ 20 kg/ha.)</li>
            <li>Pheromone traps are used to monitor and catch moths of cutworms.</li>
        </ul>
        <h3>Potato Tuber Moth (<em>Phthorimaea operculella</em>)</h3>
        <p>Potato tuber moths (PTM) attack potato in
            both field and storage.It causes 100% tuber
            damage in the farmers field and home
            storages. Small larvae usually enter the tuber
            at the eyes. Frequently, the larvae feed just
            below the surface of the potato leaving a dark
            tunnel. The tunnels are filled with larval
            excreta. Damage tuber attacked by bacterial infestation resulting potato tuber is rotted.</p>
        <h3>Control measures:</h3>
        <p>Pest incidence can be reduced through cultural practices such as - </p>
        <ul>
            <li>Controlling irrigation to prevent soil cracking that allows moths to reach the tubers.</li>
            <li>Hilling-up to properly cover the tubers. </li>
            <li>Harvested tubers are not left in the fields overnight; the heaps of tubers should be
                covered with polythene sheet / dried straw/ cloth to laying egg on the seed tubers.</li>
            <li>Using pheromone traps to capture adult moth and monitor field populations.</li>
            <li>Likewise, repellent plants such as Minthostachys spp., eucalyptus, or lantana help
                to protect stored tubers.</li>
            <li>The PTM damage can be reduced by dipping tubers in suspension of Phenthoat /
                Deltamethrin at 2 ml/L of water for 15 minutes and then drying after shade
                especially seed tubers.</li>
        </ul>
        <h3>Aphids</h3>
        <h3>Green peach aphid: <em>Myzus persicae</em></h3>
        <h3>Potato aphid: <em>Macrosiphum euphorbiae</em></h3>
        <p>Aphid colonies can be easily identified in plant terminal leaves and on the underside
            of leaves and plant root in the field. As a result black sooty mould fungus that grows
            on the honeydew produced by the nymphs will cover the plant. Due to severe
            infestation, leaf become curl and finally dried
            from upper portion of the plants.They also
            appear in tuber sprouts in stores. Finally they
            transmit viruses to seed potatoes.</p>

        <h3>Control measures:</h3>
        <ul>
            <li>Foliar sprays of Neem seed extract (1 kg, half broken neem seeds are dipped into
                10 litres water for 12 hours). </li>
            <li>Foliar spray of soap water @ 2gm /L of water at 3 days interval. </li>
            <li>Sticky yellow traps can be used for population level evaluation and control. </li>
            <li>Different predatory and parasitoid insects also feed on aphids.Common predators
                are lady bird beetles and their larvae, green lacewing larvae, and syrphid fly larvae.</li>
            <li>Schedule spaying of insecticides such as-Melathion (Fyfanon 57EC, Malataf
                57EC@ 2ml/1L of water)/Imidaclorpid (Admire 200 SL @ 0.5ml/1L of water)
                should be monitored regularly.</li>
            <li>Haulm pulling will be done for seed potato production.</li>
            <li>Rogue infected potato plants to reduce the incidence of viral disease infection
                within a field.</li>
        </ul>
        <h3>White flies </h3>
        <h3>(<em>Aleurodicus disper</em>,<em> Bemisia tabaci)</em></h3>
        <p>Besides the presence of nymphs and adults on the underside of leaves, white flies
            cause yellowing of infested leaves. Whitefly excretes honeydew covers the lower
            leaves and supports the growth of black sooty mould,
            which may coat the entire plant. Recently, potato apical
            leaf curl virus has been reported and up to 100%
            infection occurred in India. This virus transmitted
            through white flies.</p>
        <h3>Control measures:</h3>
        <ul>
            <li>Unnecessary use of insecticides should be avoided.</li>
            <li>Sticky yellow traps can be used for population level evaluation and control.</li>
            <li>Foliar sprays of Neem seed extract (1 kg, half broken neem seeds is dipped into 10
                litres water for 12 hours).</li>
            <li>Foliar spray of soap water @ 2gm /L of water at 3 days interval.</li>
            <li>Monitor on schedule spray of insecticides such as-Melathion(Fyfanon 57EC, Malataf
                57 EC @ 5 ml/1L of water) or Imidaclorpid(Admire 200SL@ 0.5ml/1L of water)</li>
        </ul>
        <h3>Leafhopper (<em>Empoasca spp.</em> and <em>Amrasca spp.</em>)</h3>
        <p>Besides the presence of nymphs and adults on the
            underside of leaves, leafhoppers burn leaf edges with
            top leaf roll and foliar yellowing. Plants may die
            prematurely. Some species transmit mycoplasmal
            diseases such as aster yellows and witches'-broom
            disease.</p>
        <h3>Control measures:</h3>
        <ul>
            <li>Avoid alternate host from the seed potato production fields.</li>
            <li>Use light trap for preventing infestation.</li>
            <li>Resistant or tolerant varieties can be used for controlling leafhopper population.</li>
            <li>Foliar spray of Neem seed extract (1 kg, half broken neem seeds is dipped into 10
                litres water for 12 hours).</li>
            <li>Schedule spray of insecticides such as- Quinalphos (Corolux 25EC, Debiqueen
                25EC and Kinalux 25EC @ 2 ml/L of water) and monitored regularly.</li>
        </ul>
