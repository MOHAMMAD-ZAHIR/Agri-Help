    <h2>Intercultural Operation</h2>

    <p>Intercultural operations are to be done in between transplanting and harvesting. It includes gap filling within
        7-10 DAT, fertilizer management, pest management, water
        management, rouging and mowing. These have been discussed in separate headings. </p>
    <p>Some of the important issues of intercultural operations are stated below:</p>

    <h3>Fertilizer management and pest control</h3>
    <ul>
        <li> Excessive use of N fertilizer favors fungal and bacterial disease development along with
            increased infestation of brown plant hopper, leaf folder, stem borer, gall midge, etc.</li>
        <li>Balanced use of potash fertilizer helps in controlling whorl maggot, green
            leafhopper, yellow stem borer, brown plant hopper, etc.</li>
    </ul>

    <h3>Water management and pest control</h3>
    <ul>
        <li>Plant hoppers and army worms could be controlled by draining field water.</li>
        <li>Alternate wetting and drying helps in controlling black bugs, plant hoppers, gall
            midge, hispa, and most stem borers.</li>
        <li>Irrigation water helps in controlling grasshoppers, thrips, ants, white grubs, mole
            crickets, root aphids, termites, root weevils and seedling maggots.</li>
    </ul>

    <h3>Pest Management</h3>
    <h4>Weed Control</h4>
    <p>Grain yield loss of rice can be mitigated if weeds, insect pests and diseases are
        controlled at the right time. As a thumb role, if rice fields are kept weed free for
        initial one third of field duration (planting to maturity) of a rice variety, then crop loss
        could be avoided. Weeds are generally controlled mechanically (hand pulling, use of
        weeder, etc) and chemically. Weed control by herbicides is more profitable than hand
        weeding. Pre-emergence and post emergence herbicides can be used for weed control.
        Examples of herbicides are provided.</p>

