        <h3>Major diseases and control measures:</h3>
        <p> <strong>Bacterial leaf blight:</strong> This disease can infect rice plant at any
            stage. Diseased seedlings die off in the bed known as krisek.
            If diseased stem is pressed at the base, sticky bad odorous
            ooze can be noticed easily. Older leaves are infected at PI
            stage. Watery symptom initiated from the tip of the leaves
            that gradually turns to yellow and finally as dried straw.
            Stormy weather and rainfall favor this disease development. </p>

        <h3>Control measures:</h3>
        <ul>
            <li>Use of balanced fertilizers.</li>
            <li>Use of additional 35-40 kg/ha potash fertilizer may cause disease symptom.</li>
            <li>Urea should not be top-dressed after hailstorms and disease development.</li>
            <li>If krisek develop, drain out water from the field and irrigate after 7-10 days.</li>
            <li>Burn straw after harvesting of diseased crop.</li>
        </ul>
        <p><strong>Ufra:</strong> This disease is caused by small thread worm like
            organism, can not be seen by naked eyes. They infect collar
            of young rice leaves. The micro organism suck sap, develop
            small white spot, which gradually become brown and leaf
            blade die out. Plant growth stunted depending on severity of
            infestation. If disease develops at booting stage, twisted
            panicles remain inside leaf sheaths. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Apply 20 kg/ha Furadan 5G or Curator 5G.</li>
            <li>Burn straw after harvest.</li>
            <li>Keep land fallow for 15-20 days after plowing.</li>
            <li>No seedbed preparation in infested field.</li>
            <li>Follow rice-non rice cropping rotation.</li>
        </ul>

        <p><strong>Tungro:</strong> It is a virus disease, which can occur at seedling to
            flowering stage. Green leaf hopper (GLH) is responsible for
            the spread of this disease. Light green line alternate with
            light yellow one appear parallel to veins in young leaves
            followed by deep yellow or yellow-orange coloration of the
            upper leaves. The infected leaves twisted slightly, plant
            growth stunted with reduced tillering. Diseased plants are
            comparatively shorter than healthier ones.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Initially remove diseased plants and burry into mud.</li>
            <li>Destroy GLH through light trapping.</li>
            <li>Control GLH by spraying Melathion 57EC at 1 L/ha, MIPC 75WP at 1.12 kg/ha, etc.</li>
        </ul>

        <p><strong>Sheath rot:</strong> A fungus disease develop on flag leaf sheath as
            round or irregular brown spot. The spot gradually increase in
            size and turn into dark grey color. Panicle emergence is
            impaired at variable degree depending on severity of the
            disease.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Burn affected stubbles.</li>
            <li>Use balanced fertilizers.</li>
            <li>Spray Nativo, Folicur, exaconazole.</li>
        </ul>

        <p><strong>Sheath blight:</strong> A fungal disease generally found at tillering
            stage. Initially grayish watery mark develops on leaf sheath.
            Centre of the speck become gray surrounded by brown
            margin. The spot gradually enlarged in whole sheath and
            parts of leaf. Hot humid weather, excessive urea application
            and densely populated plants favor disease development. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Floating stubbles after final land preparation should be
                removed and buried into soil.</li>
            <li>Apply potash fertilizer in equal two splits at final land
                preparation and at last urea top dressing.</li>
            <li>Spray Nativo, Folicur or exaconazole.</li>
        </ul>

        <p><strong>Blast:</strong> A fungus disease, which may develop in the leaf (leaf
            blast), node (node blast) and neck of panicle (neck blast).
            Oval shaped brown spot, whitish or ash color at the centre
            develop on leaves. Many spots merge together resulting in
            dead leaf. Whole plot may be damaged because of this
            disease. Low night temperature, high day temperature and
            dew in the morning favor blast disease development.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Add organic fertilizer. </li>
            <li>Keep standing water.</li>
            <li>Use seed from disease free field.</li>
            <li>Use balanced fertilizer. Suspend urea top dressing.</li>
            <li>Apply 400 gm/ha Trooper, Nativo twice in 10-15 days.</li>
        </ul>

        <h3>Major insects and control measures:</h3>
        <p>Rice varieties tolerant to insect pests should be cultivated. There are many insects in
            the rice field- some of them are predators, some are parasitoids and others are pest for
            the rice crop. So, integrated pest management (IPM) technique should be adopted to
            save beneficial insects. Control measures of some important insect pests are discussed and some insecticides
            and rates for controlling rice insects are presented in Table 2. </p>

        <p><strong>Stem borer:</strong> Stem borer affected plants produce dead heart or
            white head symptom depending on growth stage of rice
            plants. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Destroy egg mass.</li>
            <li>Control moth through light trapping.</li>
            <li>Use perching technique.</li>
            <li>Spray insecticide if 10-15% dead heart or 5% white head is observed.</li>
            <li>Burn stubbles.</li>
        </ul>

        <p><strong>Gall midge:</strong> This insect infests growing tillers, which turns
            into onion shoot.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Regular observation of field.</li>
            <li>Destroy adult insects through light trap.</li>
            <li>Use insecticide if 5% onion shoots are observed.</li>
        </ul>

        <p><strong>Rice hispa: </strong> Grub destroys internal leaf tissue and adult feed
            on green leafy parts resulting in whitish leaf. </p>
        <h4>Control measures:</h4>
        <ul>
            <li>Using sweeping net for destroying adults.</li>
            <li>Use insecticides if 35% leaves are damaged or 4
                adults/hill or 5 grubs/tiller are observed.</li>
        </ul>

        <p><strong>Rice caseworm:</strong> This insect makes small cages by cutting
            upper portion of leaves. Leaves of infested field are white.
            These cages float on water in day time.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Destruction of moths by light trap.</li>
            <li>Collect cages from water by hand net and destroy them.</li>
            <li>Drain water from the field.</li>
            <li>Use insecticides if 25% leaves are damaged.</li>
        </ul>

        <p><strong>Leaf roller:</strong> Larvae of this insect folds leaf and feeds on
            internal green parts. Burning symptom appears when leaves
            are severely damaged. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Destroy moth through light trap.</li>
            <li>Perching.</li>
            <li>Use insecticides if 25% leaves are damaged at or before PI stage.</li>
        </ul>

        <p><strong>Brown planthopper:</strong> This insect sucks cell sap from the base
            of the plant. Plants dry up showing burning symptom which
            is known as hopper burn. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Transplant at 25 cm x15cm or 20 cm x 20cm spacing for
                allowing light and air in the field followed by regular
                inspection.</li>
            <li>Use balanced amount of urea.</li>
            <li>Drain water if insect is seen.</li>
            <li>Short duration variety may be cultivated.</li>
            <li>Use insecticides if 4 gravid females or 10 grubs or both are
                found in most hills. Comm unity based control measures
                are effective.</li>
        </ul>
        <p><strong>Green leafhopper:</strong> This insect sucks leaf cell sap resulting in
            reduced plant growth. It is a virus disease carrier.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Use of light trap.</li>
            <li>Use insecticides if green leafhopper is found in every sweeping
                net and nearby existence of tungro affected plants.</li>
        </ul>

        <p><strong>Rice bug:</strong> This insect infest rice field at the milking stage of
            rice plant. Pungent smell emit from the adult insect.</p>

        <h4>Control measures:</h4>
        <ul>
            <li>Use light trap.</li>
            <li>Use insecticides in the afternoon if 2-3 rice bugs/hill are found.</li>
        </ul>

        <p><strong> Thrips:</strong> This insect infests at seedling and tillering stage of rice plant. Thrips make
            lesion on leaves and sucks cell sap resulting in leaf folding longitudinally. </p>

        <h4>Control measures:</h4>
        <ul>
            <li>Irrigate seedbed/field followed by urea topdressing.</li>
            <li>Use insecticides if severely infested.</li>
        </ul>
