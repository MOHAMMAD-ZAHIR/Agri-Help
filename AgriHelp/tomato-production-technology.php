        <h3>Climate, land and soil</h3>
        <p>The optimum temperature required for tomato is 20-250C for better production. High
            to medium high land is suitable for tomato cultivation. Tomato may be grown on a
            wide range of soil from sandy to clay. The optimum soil PH required for tomato is
            6.5-7.0. In acidic soil, liming is required.</p>
        <h3>Seed rate</h3>
        <p>200-220 g seed is required for one hectare of land.</p>
        <h3>Seed treatment</h3>
        <p>Provax 200 / Thiram 75% dust @ 3g mixed with 1 kg seed should be done and shake
            well before sowing.</p>
        <h3>Time of sowing </h3>
        <p>September- October is the optimum time for seed sowing of winter tomato.</p>
        <h3>Seed bed preparation</h3>
        <p>Tomato seedlings are raised in the nursery bed. Seed bed should be 3 m in length and
            1 m in breadth. Seed beds are prepared with the mixture of sand, compost and soil.
            Tomato seeds are sown in the line on a well prepared seed bed and lightly covered
            with soil. </p>
        <h3>Transplanting method</h3>
        <p>After 7-10 days of sowing, the young seedlings are transplanted on the second bed at
            a distance of 4 cm in both ways. The bed should be irrigated immediately after
            transplanting. The seedling should be protected from strong sun and heavy rain.
            Thirty-five days old seedlings should be transplanted in the main field at a spacing of
            60 cm * 40 cm (row * plant).</p>
        <h3>Land preparation</h3>
        <p>Tomato should be planted in well pulverized field with 4-5 times ploughing sfollowed
            by laddering. Farmyard manure/compost and recommended dose of chemical
            fertilizer are incorporated into soil during final ploughing.</p>
        <h3>Fertilizer dose and application methods</h3>
        <p>Cowdung: 10t/ha, Urea: 550 kg/ha, TSP: 450 kg/ha, MP: 250 kg/ha, Gypsum: 120
            kg/ha, Boron: 2 kg/ha. Half of cowdung, half of TSP and entire amount of gypsum
            and boron should be applied during land preparation. The remaining half of cowdung
            and TSP should be applied during pit preparation before a week of planting. The
            entire urea and MP are to be applied in 3 equal installments of 21, 35 and 50 days
            after transplanting.</p>
        <h3>Irrigation</h3>
