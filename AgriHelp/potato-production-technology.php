        <h3>Seed rate</h3>

        <p>1.5- 2.0 t/ha (whole tuber); 1.2 -1.5 t/ha (cut tuber)</p>

        <h3>Seed treatment</h3>
        <p>Seed should be treated with 3% boric acid solution before sprouting for controlling
            the common scab disease. In this case, boric acid should be melted with light heat
            water. After cooling of the boric acid solution, it should be sprayed on potato tuber. </p>
        <h3>Time of sowing</h3>
        <p>15 to 30 November is the optimum time but sowing may be done in October to December.</p>
        <h3>Land preparation</h3>
        <p>Soil should be tilth properly by ploughing and laddering.</p>
        <h3>Sowing method</h3>
        <p>Ridge to Furrow method </p>
        <h3>Fertilizer application</h3>
        <p>Cowdung 10 t/ha (at final land preparation), (i) Urea 350 kg/ha (50% at the time of
            planting + 50% at 30-35 days after planting), (ii) TSP 100 kg/ha, (iii) MP 210 kg/ha,
            (iv) Zypsum 84 kg/ha, (v) Micronutrient deficient in soil like ZnS04 and Boric acid
            may be fertilized @ 12 and 5 kg/ha, respectively as basal dose. Zn fertilizer is not
            allowed to mix with Phosphatic fertilizer.</p>
        <h3>Irrigation</h3>
        <p>Irrigations need for potato cultivation depending on the soil type. Sometimes, presowing irrigation may be
