    <h2>Harvesting and Seed Preservation</h2>
    <p>Rice should be harvested at proper ripening stage. If 80% grains in a panicle turn
        yellow, it is ready for harvesting. Threshing should be completed immediately to
        preserve quality of grains. Concrete threshing floor is better, but if is not available
        then use thin jute/bamboo mat or polyethylene on top of soil. Rice should be sun
        dried for 4-5 days followed by winnowing and storage.</p>

    <h3>Seed preservation</h3>
    <p>Preservation of good quality seed is the prime requirement for better harvest in the
        coming season. Rice field having no weed infestation, no disease and insect pest
        attack and free from off types is the right choice for seed collection. Harvest rice at
        the proper ripening stage, thrash, dry, winnow and store at right relative humidity and
        temperature. Consider following points during seed storage-</p>
    <ul>
        <li>Dry seed to reduce seed moisture content below 12%.</li>
        <li>Sort out fully filled grains while winnowing.</li>
        <li>Preserve seed in air sealed container.</li>
        <li>If earthen pot is used, coal tar may be used to coat outer surface of the container.</li>
        <li>Thick polyethylene bag can be used for seed storage.</li>
        <li>Sun dried seeds should be put into container in full after cooling in shadow place.</li>
        <li>Make the container air tight and keep it above ground.</li>
        <li>Use dried leaves of Azadiracta indica @ 3.25 kg/ton rice seed for preventing insect attack.
        </li>
    </ul>
