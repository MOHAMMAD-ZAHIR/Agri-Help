    <h2>Harvesting and Seed Preservation</h2>
    <p>In case of seed plot, the off type plants should be rouged out at least 3 times starting
        from flowering up to maturity stage to ensure varietal purity. Crop should be
        harvested at full maturity in a
        sunny day. Usually, wheat is
        harvested by hand. Wheat should
        be threshed by paddle thresher or
        power thresher. Wheat seeds
        should be dried up under sun to
        bring the moisture below 12%. It
        may be confirmed by chewing the dried grain. Grain should be cleaned and graded.
        For cleaning, paddle or power winnower can be used.</p>
    <p>After final sunning, seeds should be cooled in a shady place. Then the seeds are to be
        stored in metallic or plastic drum and polythene bag. All the drums or bags should be
        air-tight. Polythene bags should be kept inside gunny bags. All the containers should
        be placed on wooden “Danish” or “Matcha”away from the wall.</p>

