        <h3>Major diseases and control measures:</h3>
        <h4>Stemphylium blight</h4>
        <p>Stemphylium blight start with the appearance of small pin-headed light brown to tan
            coloured spots on leaflets. Under ideal conditions the small spots enlarge rapidly,
            covering the entire leaflet surface within a 2-3 day period. The infected tissue appears
            light cream in colour, often with angular
            patterns of lighter and darker areas that
            spread across, or long, the entire leaflet. The
            affected foliage and stems gradually turn dull
            yellow, giving a blighted appearance to the
            crop. The infected leaves can be abscised
            rapidly, leaving only the terminal leaflets on
            the stems. The stems bend down, dry and gradually turn ashy white, but pods remain
            green. White mycelial growth can sometimes be seen on the infected stems.</p>
        <h3>Control measures: </h3>
        <ul>
            <li>Being stubble-borne disease strategies such as destruction of old crop residues,
                and crop rotation would assist in decreasing potential inoculum sources.</li>
            <li>The most economical and sustainable strategies to control Stemphylium blight are
                through use of resistant/tolerant variety (BARIMasur-4, BARIMasur-5,
                BARIMasur-6 and BARIMasur-7). </li>
            <li>Delay sowing reduces the incidence of Stemphylium blight disease of lentil but it
                reduces the pod setting and lowering the seed yield significantly. </li>
            <li>Application of Rovral (Iprodion) 50 WP (0.2%) or Secure 600 WG (Fenamidone +
                Mancozeb), Companion(Mancogeb+Canendazim) performs better to control the
                disease effectively when sprayed 3 times at an interval of 10 days starting from the
                initiation of the disease.</li>
            <li>Integrated Disease Management include the use of healthy seed and Secure 600
                WG spray with wider spacing (40 cm) performed higher yield through lower
                disease incidence.</li>
        </ul>
        <h3>Foot rot </h3>
        <p>Foot rot occurs in the field as patches of
            dead plants, most often at the seedling
            stage. The pathogen attacks the collar
            region of the plant causing slight yellows
            brown discoloration and rotting of the
            tissue. The young seedling shows damping off symptom. The infected crop become chlorotic, quickly dies and
            dries up. The
            pathogens are soil borne and incidence of the disease has been observed to be higher
            in soil with initially high moisture content.</p>
        <h3>Control measures: </h3>
        <ul>
            <li>Use of tolerant variety (BARIMasur-5, BARIMasur-6 and BARIMasur-7).</li>
            <li>Seed treatment with Provax-200 @ 0.25% w/w of dry seed.</li>
            <li>Changing the seeding date and tillage method was found effective to reduce the
                incidence of diseases. Optimum sowing at the beginning of November found better
                throughout the country.</li>
            <li>Soil treatment with Trichoderma spp. and/or Rhizobium spp. inoculum @ 210 g/ha
                showed better result against foot and root rot. </li>
        </ul>
        <h3>Rust </h3>
        <p>Rust starts with the formation of yellowish-white pycnidia and aecial cups on the lower
            surface of leaflets and on pods, singly or in
            small groups in a circular form. Later,
            brown uredial pustules emerge on either
            surface of leaflets, stem and pods. Pustules
            are oval to circular and up to 1 mm in
            diameter. They may coalesce to form larger pustules. The telia, which are formed late
            in the season, are dark brown to black, elongated and present mainly on branches and
            stems. In severe infections leaves are shed and plants dry prematurely, the affected
            plant dries without forming any seeds in pods or with small shriveled seeds. The plant
            has a dark brown to blackish appearance, visible in affected patches of the paddock or
            in the whole paddock if totally infected.</p>

        <h3>Control measures:</h3>
        <ul>
            <li>Use of tolerant variety (BARI Masur-5, BARI Masur-6 and BARI Masur-7).</li>
            <li>In the northern districts of Bangladesh, the diseases occur at the end of February.
                The disease can be avoided by selecting early maturing varieties like BARI Masur-1 as well as by
                adjusting sowing date i.e. by the first week of November in the
                northern and last week of October in the southern parts of the country.</li>
            <li>Seed treatment with Provax-200 @ 0.25% w/w of dry seed as well as protective
                foliar spray with Tilt (Propiconazol) 250 EC (0.05%) performed well.</li>
            <li>Integrated Disease Management includes use of healthy seed + tolerant variety +
                Seed treatment + Tilt spray + wider spacing (40cm). </li>
        </ul>
        <h3>Major insects and control measures:</h3>
        <h3><em>Aphid (Aphis craccivora Koch)</em> </h3>
        <p>Aphid is the key pest of lentil in the field condition. It injures lentils mostly by
            direct feeding. Direct feeding by the insect includes sucking sap from leaves, stems,blossoms and pods.
            Plants heavily fed upon are
            stunted and produce fewer and smaller pods and
            seeds. Smaller plants may die. Normally10-15% yield
            may be lost by aphid infestation.</p>

        <h3>Control measures:</h3>
        <ul>
            <li>To control aphid infestation avoid late sowing, if
                late, completely avoid mixed crop with mustard.
                Yellow pan trap can control aphid, which is made
                by soap dust mixed with water in yellow pan.</li>
            <li>Conserve natural enemies (Lady beetle, syrphid fly etc.) in the crop field by
                minimum or no use of insecticides for the enhancement of biological control.</li>
            <li>Spraying of detergent (Jet powder @ 5 g/l water) or neem seed extract @ 50 g/l at
                5-7 days interval.</li>
            <li> In case of severe infestation, Melathion 57 EC or Dimethoate 40 EC @ 2 ml/l can
                be sprayed twice at 7 days interval.</li>
        </ul>
