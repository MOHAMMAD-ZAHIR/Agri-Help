        <h2>Production Technology</h2>
        <h3>Land and Soil</h3>
        <p>Sandy loam along with high land is the best suitable for sesame cultivation but it
            could not survive in water logging condition. </p>
        <p><strong>Seed rate:</strong> 7 kg/ha</p>
        <h3>Seed treatment</h3>
        <p>Seeds should be treated with fungicide before sowing because seeds often bear
            diseases pathogens which hamper seedling growth and development. So, seed
            treatment is very important for maintaining normal plant growth and healthy
            seedling. Vitavex-200, Bavistin, Captan and Homai should be used as for seed
            treatment. A big container with lid could be used for seed treatment. Fungicide @ 2
            g/kg of seeds or any other recommended rate should be used. Seeds should be sown
            on the following day after proper mixing.</p>
        <h3>Time of sowing</h3>
        <p>For kharif-1 season, the optimum sowing time is end of February to middle of April
            and for autumn crop the optimum sowing time is mid August to mid September.
            However fork harif-1 (summer) season, early sowing is advocated for sesame
            cultivation to achieve higher yield.</p>
        <h3>Land preparation</h3>
        <p>Land should be prepared by 4-5 times ploughing followed by laddering. Land should
            be free from big clods and weeds. Land should be levelled, and drains should be
            made for easy drainage of excess rain or irrigation water. In the raised bed system,
            drainage of excess water could easily be controlled.</p>
        <h3>Sowing method</h3>
        <p>In case of line sowing, line to line distance is 30 cm and seeds are sown continuously
            in line whileafter emergence, plant to plant distance should be kept at 5cm.</p>
        <h3>Fertilizer dose and application method </h3>
        <p>Fertilizer dose varies depending on soil fertility and also differs in different agro-ecological zones (AEZ).
            However, fertilizer dose for sesame crop is Urea: 100-125;
            TSP: 130-150; MP: 40-50; Gypsum: 100-110; Zinc sulphate: 0-5 and Boric acid: 8-10
            kg/ha, respectively. Fifty percent urea and full dose of other fertilizers should be
            applied at the time of final ploughing. The rest amount of urea should be top dressed
            at 25-30 days after sowing i.e. before flowering. Adequate soil moisture is necessary
            during top dressing.</p>
        <h3>Irrigation</h3>
        <p>Generally, irrigation is not required for sesame cultivation. But for kharif-1 crop,
            irrigation may be required at initial stage of the crop because of late rain. So, one
            irrigation would be required before flowering at 25-30 days after sowing. Sesame
            crop could not tolerate water logging. So, irrigation water or rain water should be
            drained out by making drains in the field immediately after irrigation or rainfall.
        </p>
