        <h2>Production Technology</h2>
        <h3>Land and soil</h3>
        <p>A deep loamy soil, high in organic matter and plant nutrients is the best soil for maize
            production. However, with proper management and fertilizer practices, the variety
            can be grown successfully on any soil from loamy sand to clay. The soil should be
            free from salinity and water logging. It can be grown successfully on soils with a PH
            from 5.0- 8.0, but 6.0-7.0 is optimum. If the soil PH is low (below 5), liming might be
            necessary.</p>

        <h3>Seed rate</h3>
        <p>The recommended seed rate is 20-22 kg/ha for grain crop but for a fodder crop seed
            rate would be increased to 70-90 kg/ha. In Bangladesh, farmers use between 20 to 30
            kg/ha seed. For Khoibhutta and Sweet Corn the recommended seed rate is 10-12 kg/ha.</p>

        <h3>Seed treatment</h3>
        <p>The most common fungicides used for seed treatment are Furadan and Arasan M.
            Seed could be treated by 50 ml of Furadan 30% (seed treater), 2g of Arasan 75%
            (wettable powder) and 12 ml of water per kilogram of seed. Seed can also be treated
            by vitavax-200@2.5g/kg seed.</p>

        <h3>Time of sowing</h3>
        <p>Sowing dates should be chosen to avoid risky environmental conditions such as
            excessively cool or hot temperatures and during heavy shower. In Bangladesh,
            optimum time of sowing in rabi season is from mid October to 1st week of
            December, in kharif I season from mid February to end of March.</p>

        <h3>Land Preparation</h3>
        <p>The variety can be cultivated with normal land preparation or with minimum or zero
            tillage. Under normal condition four to five ploughings followed by laddering are
            adequate for sowing.</p>

        <h3>Sowing method</h3>
        <p>Seeds should be sown in rows. Usually, sowing is done in furrows in light textured
            soils and in ridges in heavy soils. In flood prone areas sowing can be done by
            dibbling method under zero tillage conditions after receding of flood water. It is
            recommended row to row 75 cm apart with a plant to plant spacing of 20 cm with
            one plant per hill which gives 66,666 plants/ha.</p>

        <h3>Fertilizer application</h3>
        <p>Fertilizer application depends on fertility status of the soil where the crop could be
            grown as well as the nutrient requirement of the variety. However, for composite and
            hybrid varieties the dose of Urea, TSP, MP, Gypsum, Zinc sulphate and Boric acid,
            respectively, are: 260, 177, 133, 97, 13.89, 5 and 543, 266, 200, 208, 13.89, 5 kg per
            hectare. Time and method of fertilizer application influence maize yield. One third of
            urea and other fertilizers should be applied at the time of final land preparation.
            Remaining two third of urea should be applied in two installments: One-third at tassel
            initiation (8-10 leaf stage) and the rest at about one week before silking or grain
            filling stage. In case of dibbling method of sowing, initial dose of fertilizers can be
            applied in the hole, 10 cm apart from the plants. The second and third doses of urea
            can be applied at 8-10 leaf stage and one week before silking. For better yield, 5-7 ton
            of cowdung per hectare should be applied.</p>

        <h3>Irrigation</h3>
        <p>The total amount of water required for the development of the variety varies with
            local conditions. Generally, water requirement is higher in hot, dry and low rainfall
            areas, and lower in cooler and more humid areas.</p>
        <p>In rabi (winter season), residual soil moisture stored from monsoon can support a good
            stand of maize seeds and dibbled during September and October in the flood-prone
            areas, once flood water recedes. Water stress at the time of germination, floral
            initiation, anthesis, and grain filling stage would reduce grains yield considerably. One
            irrigation at each of these growth stages i.e. within a day of seeding; at 8-10 leaf stage;
            a week before silking or grain filling stage, is necessary for high yield. Waterlogging
            at any stages of growth is harmful so, drainage is essential for summer crop.</p>
        <p>In kharif1 (summer season), there is no need to irrigate except during prolonged
            drought period. However, if the crop is sown before rains, a pre-sowing irrigation is required for
            germination and good subsequent growth. Under heavy rainfall
            conditions, drainage should be provided.</p>
