    <h2>Intercultural Operation</h2>
    <div>
        <p>For a good stand of maize, thinning at crop establishment stage may be required. In
            case of over-planting, thinning to a desired number should be done within two weeks
            of germination or when the seedlings are about 15 cm tall. Care should be taken not
            to disturb standing plants at the time of thinning operation.</p>

        <p>Earthing up of plants is one of the most important operations in maize cultivation.
            Earthing up means placing of soil near the base of the plant collected from the space
            between the rows. This operation helps to provide anchorage of the lower whorls of
            adventitious roots above the soil which then begin to function as absorbing roots. This
            operation also prevents the plants from lodging. The furrows made out of this operation
            could be used as drainage or irrigation channels, depending on the requirements. This
            operation can be performed with the help of spade at the time of application of the
            second dose of urea at 8-10 leaf stage of the crop.</p>

        <p>Weed control is essential to ensure good harvest. In Bangladesh, weeds are not a
            serious problem in the winter season but in the summer season weeding is necessary.
            When the seedlings are about 2 weeks old, first weeding should be done. Another 2-3
            weedings may be required depending on the degree of weed infestations during
            different life cycle of the crop. Maize farmers of Bangladesh practice hand weeding
            when weed infestation is very high.</p>

        <h3>Pest Management</h3>
        <p>Although insects and diseases are not a serious problem in the country but the
            situation is changing with the increasing of area and intensity of maize cultivation.
            The important diseases appear during seedling, growing and maturity stages of maize
            are given below:</p>

        <h4>Major diseases and control measures:</h4>
        <p>Major diseases are:</p>
        <ol>
            <li>Seed rot and seedling blights;</li>
            <li>Leaf blight;</li>
            <li>Yellow leaf blight;</li>
            <li> Stalk rots; and</li>
            <li> Ear rot and kernal rots.</li>
        </ol>
        <h4>Control measures:</h4>
        <ul>
            <li>Application of proper fungicide should be done to control those diseases.</li>
            <li>Tilt 250 EC @ 0.05% should be applied to control leaf blight diseases, starting
                when lesions are first observed.</li>
        </ul>
        <h3>Major insects and control measures</h3>
        <p>Major insects are:</p>
        <ol>
            <li>Maize aphid;</li>
            <li>Corn earworm;</li>
            <li>Cut-worms;</li>
            <li>Stem borer; </li>
            <li>Seedling maggot and</li>
            <li>Hairy caterpillar</li>
        </ol>
        <h4>Control measures:</h4>
        <ul>
            <li>For controlling cutworm, field irrigation, application of poison bait or application of
                granular insecticides @ 1.5 kg a.i/ha should be done.</li>
            <li>Dursban, Pyrifos 20 EC @ 5 ml/litre of water should be sprayed to control soil
                borne insects. </li>
            <li>Marshall 20 EC or Diazinon 60 EC @ 2ml/litre water could be sprayed properly to
                control stem borer insects.</li>
            <li>For ear worm, the larvae could be killed after collecting from the infested cobs.
                Cypermethrin (Ripcord 10 EC/ Cymbush 10 EC/ Fenom 10 EC) @ 2ml/litre water
                could be sprayed to a control this pest.</li>
        </ul>
