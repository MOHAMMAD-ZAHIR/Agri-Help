    <h2>Intercultural Operation</h2>
    <div>
        <p>Cultural operation keeps the field absolutely free from weeds to produce a good crop
            of onion. Onions are one of the vegetable/spice crops most prone to weed infestation.The crops are poor
            competitors against weeds due to its slow establishment and a
            canopy of short upright leaves. Henceforth, weeding is the most important activity for
            this crop.
        </p>
        <h3>Special operation for seed production</h3>
        <h3>Earthing up</h3>
        <p>Soils around the plants should be done to protect seed crops from fall down.</p>
        <h3>Staking</h3>
        <p>Staking is one of the most important activities for intercultural operation in onion
            seed crop. Without this activity the program of onion seed production may be failed
            due to breaking of flower stalks by strong wind. Staking should be provided around
            the unit plots at 65 days after planting of seed bulbs with bamboo sticks and jute cord
            to keep the flower stalk erect and to protect them from strong wind. Breaking the
            flower stalks leads to produce higher percentage of chaffy seeds resulting in lower
            quality and low yield of onion seed.</p>
        <h3>Pest management</h3>
        <h4>Major insects and control measures:</h4>
        <p><strong>Onion Thrips</strong> (<em>Thrips tabaci</em>): The most common and serious pests are found in
            onion production. During dry and warm weather thrips feed on leaf surfaces and
            leaves become white. The insect has many host plants and adults and nymphs over
            winter on plants or plant debris or in weeds bordering the field.</p>
        <h4>Control measures: </h4>
        <ul>
            <li>Preventive measures should be taken including destruction of weeds as they are
                alternate hosts.</li>
            <li>Badly infested leaves should be removed.</li>
            <li>The crop should be sprayed with insecticides like Admire/Score @ 2 ml/l of water
                at 15 days interval to keep the seed crops free from insect-pests.</li>
        </ul>
        <h3>Bolting and sprouting in storage:</h3>
        <p>Application of Maleic hydrazide (MH) checks premature bolting and sprouting in
            storage. Spraying MH @ 2000-2500 ppm just before neck fall in rabi onion are
            recommended</p>
